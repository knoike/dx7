fn midinote_to_logfreq(note: i32) -> i32 {
    let log2_440: f64 = 440.0f64.log2();
    let logfreq = (log2_440 + (note as f64 - 69.0) / 12.0) * ((1i64 << 24) as f64);
    (logfreq + 0.5).floor() as i32
}

