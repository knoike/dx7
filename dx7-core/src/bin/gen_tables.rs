fn gen_detune_tab() {
    let mut tab = [0i32; 128];
    for midinote in 0..128i32 {
        let logfreq = midinote_to_logfreq(midinote);
        let logfreq_frac = logfreq as f64 / (1i64 << 24) as f64;
        let detune_ratio = 0.0209 * (-0.396 * logfreq_frac).exp() / 7.0;
        tab[midinote as usize] = (detune_ratio * logfreq as f64) as i32;
    }
    print_i32_array("DETUNE_TAB", &tab);
}

